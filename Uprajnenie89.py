#Функция которая переводит числа в числительные в диапазоне от 1 до 12.
def Numerals(Number):
    if Number == 1:
        return 'First'
    elif Number == 2:
        return 'Second'
    elif Number == 3:
        return 'Third'
    elif Number == 4:
        return 'Fourth'
    elif Number == 5:
        return 'Fifth'
    elif Number == 6:
         return 'Sixth'
    elif Number == 7:
        return 'Seventh'
    elif Number == 8:
        return 'Eight'
    elif Number == 9:
        return 'Ninth'
    elif Number == 10:
        return 'Tenth'
    elif Number == 11:
        return 'Eleventh'
    elif Number == 12:
        return 'Twelfth'
    else:
        print('')

